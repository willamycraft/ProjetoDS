    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        int opcao = tabela.getSelectedRow();
        if(opcao >= 0){
            edProduto.setText(tabela.getValueAt(opcao, 0).toString());
            edValor.setText(tabela.getValueAt(opcao, 2).toString());
            jTabbedPane1.setSelectedIndex(0);
            btAtualizar.setVisible(true);
            btSalvar.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
    }//GEN-LAST:event_btEditarActionPerformed
    }
    private void btApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btApagarActionPerformed
        int opcao = tabela.getSelectedRow();
        if (opcao >= 0) {
            Produto p = new Produto();
            p.setCod_prod(Integer.parseInt(tabela.getValueAt(opcao, 0).toString()));
            p.setNome_prod(tabela.getValueAt(opcao, 1).toString());
            ProdutoDao pdao = new ProdutoDao();
            pdao.deletar(p);
            preencherTabela();
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }
    }//GEN-LAST:event_btApagarActionPerformed

