
package mapeamento;


public class ReceberContas {
    private int cod_rec;
    private int cod_ven;
    private String dia_rec;
    private String hora_rec;
    private float valor_rec;

    public int getCod_rec() {
        return cod_rec;
    }

    public void setCod_rec(int cod_rec) {
        this.cod_rec = cod_rec;
    }

    public int getCod_ven() {
        return cod_ven;
    }

    public void setCod_ven(int cod_ven) {
        this.cod_ven = cod_ven;
    }

    public String getDia_rec() {
        return dia_rec;
    }

    public void setDia_rec(String dia_rec) {
        this.dia_rec = dia_rec;
    }

    public String getHora_rec() {
        return hora_rec;
    }

    public void setHora_rec(String hora_rec) {
        this.hora_rec = hora_rec;
    }

    public float getValor_rec() {
        return valor_rec;
    }

    public void setValor_rec(float valor_rec) {
        this.valor_rec = valor_rec;
    }
    
    
}
